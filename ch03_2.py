# 나누어 떨어지는 숫자
int = int(input("정수를 입력해 주세요: "))

if int % 2 == 0:
    print(f"{int}는 2로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int}는 2로 나누어 떨어지는 숫자가 아닙니다.")

if int % 3 == 0:
    print(f"{int}는 3으로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int}는 3으로 나누어 떨어지는 숫자가 아닙니다.")

if int % 4 == 0:
    print(f"{int}는 4로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int}는 4로 나누어 떨어지는 숫자가 아닙니다.")

if int % 5 == 0:
    print(f"{int}는 5로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int}는 5로 나누어 떨어지는 숫자가 아닙니다.")